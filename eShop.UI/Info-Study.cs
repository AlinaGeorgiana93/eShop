//Semantisk tagg = beskriver vad kontainers ska innehålla. <div> med andra namn:
// <article> = Den här ska innehålla en viss typ av data.(blogg-post, sektion på sidan).
// <section> = En större del av article, större sektion.Kan vara inne i article
// <aside> = används för att skapa menyer och länklista på sidan. 
// <audio> = ljud, spela en mp3 fil
// <bdi> = texten ska formateras till ett annat språk
// <canvas> = med javascript kan man rita på skrämen 
// <datalist> = en inputlista man kan välja olika alternativ. Modernare dropdown
// <header> = Högst upp, sektionsdel man kan använda när man skapar en meny/artikel/sidan och vill ha en rubrik.
// <nav> = där menyn finns för navigering.


// ~ = betyder att det är en relativ sökväg ifrån roten.
// <a> = hyperlänkar som man kan klicka på 
// <adress> = speciell typ av tag ifall man vill formatera en adress så den ser ut på ett visst sätt (Semantisk tagg) 
// <b> = boald (fetstil), används sällan. (Strong används istället) 
// <body> = där vi skriver vår html. 
// <br> = rak linje över skärmen. används sällan.
// <button> = en knapp.
// <dl> <dd> <dt> = Visar en lista av värdne, (produktlista, kan visa en produkts innehåll rubriker på vänster sida och text på höger sida).
// <div> = är en kontainer/element med öppen- och sluttagg, man kan lägga in listor, knappar, länkar, text emellan och få en radbrytning efter.
// <form> = en formulär. 
// <h1>-<h6> = heading, rubriker i olika storlekar h1 är störst.
// <head> = där vi konfiguerar tex bootstrap. 
// <hr> = en linje rakt över sidan, avskiljningslinje.
// <img> = bilder (scr = en source som pekar vart bilden finns, alt = en beskrivande text av bilden).
// <label> = används oftast tillsammans med en textbox i ett formulär.(rubriken i en textbox).
// <li> = Punkt/Numrerad-lista. li har vi som varje punkt i listan. ligger allid i en OL(ordered list) eller UL(Unordered list).
// <meta> = metadata.
// <ol> = kontainern för att göra lista med li-element. ordered list. (numrerad lista)
// <ul> = kontainern för att göra lista med li-element. unordered list. (punkt lista) 
// <p> = paragraf med text.
// <script> = om man vill ha javascript (ha javascript filer i wwwroot som länkas in via index.html).
// <select> = dropdown lista.
// <span> = en kontainer som kan innehålla olika information. En kontainer som följer med texten. Används inne i en text för att styla specifika delar.
// <strong> = framhäva något, default fetstil 
// <style> = om man vill lägga in css-styling 
// <table> = bygger en tabell med tre sektioner ( <thead> = rubrik , <tbody> = info under rubrik, <tfoot> = beskrivning under tabell. Inne i dessa kan vi använda <td> = cell , <th> = rubrik, <tr> = ny rad
